SELECT 
    SE2.E2_FILIAL AS FILIAL, -- 1
    SD1.D1_PEDIDO AS PEDIDO, -- 2
    SE2.E2_FILIAL AS FILSE2, -- 3
    SE2.E2_PREFIXO AS PREFIXO, -- 4
    SE2.E2_NUM AS TITULO, -- 5 
    SE2.E2_PARCELA AS PARCELA, -- 6
    SE2.E2_TIPO AS TIPO, -- 7
    SE2.E2_FORNECE AS FORNECE, -- 8
    SE2.E2_LOJA AS LOJA, -- 9 
    SE2.E2_NOMFOR AS NOMFOR, -- 20
    SA2.A2_NOME AS NOME, -- 11
    SA2.A2_ZZDRE AS DRE, -- 12
	SD1.D1_DTDIGIT AS DTINCLUSAO, -- 13
    SE2.E2_EMISSAO AS DTEMISNF, -- 14
    SE2.E2_MOVIMEN AS DTMOVIMEN, -- 15
    SE2.E2_VENCTO AS DTVENCTO, -- 16
    SE2.E2_VENCREA AS DTVCREAL, -- 17
    SE2.E2_BAIXA AS DTBAIXA, -- 18
	SE2.E2_VALOR AS VALTITULO, -- 19
    SE2.E2_HIST AS HISTORICO, -- 20
	CASE 
        WHEN SD1.D1_ZZUSER IS NULL THEN SE2.E2_ZZUSER 
        ELSE SD1.D1_ZZUSER 
    END AS USUARIO,
    SE2.E2_USERLGI AS USERLGI,  -- 22
    SE2.E2_NATUREZ AS NATUREZA,  -- 23
    SEA.EA_NUMBOR AS NUMBORD, --24
	CASE 
		WHEN SE2.E2_ORIGEM = 'MNTA765' THEN 'Multas'
		WHEN SE2.E2_ORIGEM = 'FINA091' THEN 'Baixa Automática'
		WHEN SE2.E2_ORIGEM = 'CNTA100' THEN 'Manutenção de Contratos'
		WHEN SE2.E2_ORIGEM = 'MATA100' THEN 'Nota Fiscal de Entrada'
		WHEN SE2.E2_ORIGEM = 'MATA460' THEN 'Documento de Saída'
		WHEN SE2.E2_ORIGEM = 'FINA378' THEN 'Aglutinação'
		WHEN SE2.E2_ORIGEM = 'MNTA766' THEN 'Notificações'
		WHEN SE2.E2_ORIGEM = 'FINA050' THEN 'Contas a Pagar'
		WHEN SE2.E2_ORIGEM = 'FINA290' THEN 'Aglutinação'
		WHEN SE2.E2_ORIGEM = 'FINA090' THEN 'Baixa Automática'
		WHEN SE2.E2_ORIGEM = 'GPEM670' THEN 'RH'
		WHEN SE2.E2_ORIGEM = 'CNTA121' THEN 'Gestão de Contratos'
		WHEN SE2.E2_ORIGEM = 'FINA080' THEN 'Baixas a Pagar Manutenção'
		WHEN SE2.E2_ORIGEM = 'FINA290M' THEN 'Faturas a pagar'
		ELSE '' 
	END AS ORIGEM,
	CASE 
        WHEN SA2.A2_MSBLQL = 1 THEN 'Sim' 
        ELSE 'Não' 
    END AS BLOQUEADO
FROM SE2010 SE2
LEFT JOIN SF1010 SF1 
    ON SE2.E2_FILIAL = SF1.F1_FILIAL 
    AND SE2.E2_NUM = SF1.F1_DOC 
    AND SE2.E2_FORNECE = SF1.F1_FORNECE
	AND SE2.E2_PREFIXO = SF1.F1_PREFIXO
    AND SE2.E2_LOJA = SF1.F1_LOJA 
    AND SF1.D_E_L_E_T_ = ' '
LEFT JOIN SD1010 SD1 
    ON SF1.F1_FILIAL = SD1.D1_FILIAL 
    AND SF1.F1_DOC = SD1.D1_DOC
    AND SF1.F1_FORNECE = SD1.D1_FORNECE 
    AND SF1.F1_LOJA = SD1.D1_LOJA 
    AND SD1.D_E_L_E_T_ = ' '
LEFT JOIN SA2010 SA2 
    ON SA2.A2_COD = SD1.D1_FORNECE
    AND SA2.A2_LOJA = SD1.D1_LOJA 
    AND SA2.D_E_L_E_T_ = ' '
LEFT JOIN SEA010 SEA
	ON SEA.EA_NUM = SE2.E2_NUM
    AND SEA.EA_FILIAL = SE2.E2_FILIAL
    AND SEA.EA_FORNECE = SE2.E2_FORNECE
	AND SEA.EA_PARCELA = SE2.E2_PARCELA
    AND SEA.EA_LOJA = SE2.E2_LOJA
    AND SEA.EA_PREFIXO = SE2.E2_PREFIXO
	AND SEA.D_E_L_E_T_ = ' '
WHERE SE2.D_E_L_E_T_ = ' ' 
  AND SE2.E2_FILIAL BETWEEN '' AND 'ZZZZ'
  AND SE2.E2_VENCREA BETWEEN '20250101' AND '20250116'
  --AND SE2.E2_NUM = '000005507'
GROUP BY 
    SE2.E2_FILIAL, SD1.D1_PEDIDO, SE2.E2_VALOR, SE2.E2_FILIAL, SE2.E2_PREFIXO, 
    SE2.E2_NUM, SE2.E2_PARCELA, SE2.E2_TIPO, SE2.E2_FORNECE, SE2.E2_LOJA, 
    SE2.E2_NOMFOR, SA2.A2_NOME, SA2.A2_ZZDRE, SE2.E2_EMISSAO, SE2.E2_MOVIMEN, SD1.D1_DTDIGIT, 
    SE2.E2_VENCTO, SE2.E2_VENCREA, SE2.E2_BAIXA, SE2.E2_HIST, SD1.D1_ZZUSER, SE2.E2_ZZUSER,  
    SE2.E2_USERLGI, SE2.E2_NATUREZ, SEA.EA_NUMBOR, SA2.A2_MSBLQL, SE2.E2_ORIGEM
ORDER BY E2_VENCREA