SELECT       
    TR6.TR6_PLACA,
    TR6.TR6_QTDCOM,
    CASE 

        -- Tipos de combustives Diesel
        WHEN TR6_TIPCOM = '0' THEN '1011.0001'
        WHEN TR6_TIPCOM = '0A' THEN '1011.0001'

        WHEN TR6_TIPCOM = '3A' THEN '1011.0001'
        WHEN TR6_TIPCOM = '3P' THEN '1011.0001'

        WHEN TR6_TIPCOM = '7' THEN '1011.0001'
        WHEN TR6_TIPCOM = '7P' THEN '1011.0001'

        WHEN TR6_TIPCOM = '11' THEN '1011.0001'
        WHEN TR6_TIPCOM = '11E' THEN '1011.0001'
        WHEN TR6_TIPCOM = '11A' THEN '1011.0001'
        WHEN TR6_TIPCOM = '11P' THEN '1011.0001'
        WHEN TR6_TIPCOM = '11F' THEN '1011.0001'

        WHEN TR6_TIPCOM = '12' THEN '1011.0001'
        WHEN TR6_TIPCOM = '12P' THEN '1011.0001'

        WHEN TR6_TIPCOM = '18' THEN '1011.0001'
        WHEN TR6_TIPCOM = '18T' THEN '1011.0001'

        WHEN TR6_TIPCOM = '19' THEN '1011.0001'
        WHEN TR6_TIPCOM = '19T' THEN '1011.0001'

        WHEN TR6_TIPCOM = '59' THEN '1011.0001'
        WHEN TR6_TIPCOM = '59A' THEN '1011.0001'


        -- Tipos de combustives Arla
        WHEN TR6_TIPCOM = '10' THEN '1021.0001'
        WHEN TR6_TIPCOM = '10E' THEN '1021.0001'
        WHEN TR6_TIPCOM = '10A' THEN '1021.0001'
        WHEN TR6_TIPCOM = '10P' THEN '1021.0001'
        WHEN TR6_TIPCOM = '10F' THEN '1021.0001'

        WHEN TR6_TIPCOM = '12' THEN '1021.0001'
        WHEN TR6_TIPCOM = '12A' THEN '1021.0001'
        WHEN TR6_TIPCOM = '12AE' THEN '1021.0001'
        WHEN TR6_TIPCOM = '12E' THEN '1021.0001'

        WHEN TR6_TIPCOM = '33' THEN '1021.0001'
        WHEN TR6_TIPCOM = '33E' THEN '1021.0001'

        WHEN TR6_TIPCOM = '34' THEN '1021.0001'
        WHEN TR6_TIPCOM = '34E' THEN '1021.0001'

        WHEN TR6_TIPCOM = '37' THEN '1021.0001'
        WHEN TR6_TIPCOM = '37E' THEN '1021.0001'

        WHEN TR6_TIPCOM = '20' THEN '1021.0001'
        WHEN TR6_TIPCOM = '20T' THEN '1021.0001'

    END AS COD_PROD
FROM  NNS010 NNS
INNER JOIN TR6010 TR6 ON NNS.NNS_TFCOMB = TR6_NUMABA AND TR6.D_E_L_E_T_ <> '*'
AND NNS.D_E_L_E_T_ <> '*'
WHERE NNS.NNS_FILIAL = %exp:cFilOrig% AND NNS.NNS_COD = %exp:cCodOrig% AND NNS.D_E_L_E_T_ <> '*'