SELECT 
		TRIM(SC7.C7_ITEM) C7_ITEM, 
        TRIM(SC7.C7_NUM) C7_NUM, 
        SC7.C7_PRODUTO,
		SC7.C7_UM,
        TRIM(SB1.B1_DESC) B1_DESC, 
		TRIM(SC7.C7_XMARCA) C7_XMARCA,
        SC7.C7_QUANT, 
        SC7.C7_SEGUM, 
        SC7.C7_QTSEGUM, 
        SC7.C7_PRECO,
		SC7.C7_TOTAL,
		SUM(SC7.C7_TOTAL) OVER () AS TOTAL_GERAL,
		SC7.C7_OP,
		SC7.C7_PLACA,
        TRIM(SC7.C7_FORNECE) C7_FORNECE,
        TRIM(SA2.A2_NOME) A2_NOME, 
        TRIM(SA2.A2_INSCR) A2_INSCR, 
        TRIM(SA2.A2_EST) A2_EST, 
        TRIM(SA2.A2_DDD) A2_DDD, 
        SA2.A2_TEL,  
        TRIM(SC7.C7_NUMSC) C7_NUMSC, 
        TRIM(SC7.C7_LOJA) C7_LOJA, 
        TRIM(SA2.A2_END) A2_END, 
        TRIM(SA2.A2_CGC) A2_CGC, 
        TRIM(SA2.A2_CEP) A2_CEP, 
        TRIM(SA2.A2_MUN) A2_MUN, 	
		TRIM(SC7.C7_ITEMSC) C7_ITEMSC, 
        SC7.C7_VALFRE, 
        TRIM(SA2.A2_EMAIL) A2_EMAIL, 
        TRIM(SA2.A2_CONTATO) A2_CONTATO,		
        SC7.C7_DATPRF,  
        TRIM(SC7.C7_EMISSAO) C7_EMISSAO, 
        TRIM(SC7.C7_OBS) C7_OBS, 
        TRIM(SC7.C7_SOLICIT) C7_SOLICIT, 
        TRIM(SA2.A2_BAIRRO) A2_BAIRRO, 
        TRIM(SC7.C7_ZZUSUIN) C7_ZZUSUIN,	
        TRIM(SC7.C7_ZZRLGI) C7_ZZRLGI, 
        SC7.C7_FRETE, 
		TRIM(CONCAT(ISNULL(SC7.C7_OBS, ''), ' | ', ISNULL(CAST(CAST(SC7.C7_OBSM AS VARBINARY(8000)) AS VARCHAR(8000)), ''))) AS OBS, 
        TRIM(SC7.C7_DESCRI) C7_DESCRI,  
        SC7.C7_DESPESA, 
        SC7.C7_VLDESC, 
        TRIM(SC7.C7_TPFRETE) C7_TPFRETE, 
        TRIM(SX5.X5_DESCRI) X5_DESCRI, 
        TRIM(SC7.C7_FILENT) C7_FILENT
FROM SC7010 SC7                                            
INNER JOIN SB1010 SB1 ON SB1.B1_COD = SC7.C7_PRODUTO AND SB1.D_E_L_E_T_ = ''
INNER JOIN SA2010 SA2 ON SA2.A2_COD = SC7.C7_FORNECE AND SA2.A2_LOJA = SC7.C7_LOJA AND SA2.D_E_L_E_T_ = ' '                      
LEFT JOIN SX5010 SX5 ON SX5.X5_TABELA = 'Z2' AND SX5.X5_CHAVE = SC7.C7_ZZMODPG AND SX5.D_E_L_E_T_ = ' ' 
WHERE SC7.C7_NUM = '059172' AND C7_CONAPRO = 'B'                 
AND SC7.D_E_L_E_T_ = ' '                                                    
ORDER BY 1			                                                        